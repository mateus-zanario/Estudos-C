// declaração de bibliotecas
#include <stdio.h>  // responsável pela escrita e leitura do programa
#include <stdlib.h> // biblioteca auxiliar

// começar o programa com a função principal
// todos os nossos comandos ficarão dentro dessa função
int main()
{
  // impressão de uma frase na tela
  printf("Hello World!\n"); // todo comando deve terminar com um ";"

  // função para pausar o programa
  system("pause"); // comando da biblioeca auxiliar

  // o último comando dos nosso programa sempre será retun 0
  return 0;
}